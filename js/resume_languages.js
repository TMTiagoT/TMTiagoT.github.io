function set_allText() { //set all texts based on language selected
	var lang = language_toggle.checked ? "pt_br" : "en_us";
	var lang_texts = language_texts[lang];
	
	for (var id in lang_texts) {
		var page_element = document.getElementById(id);
		if(page_element) page_element.innerHTML = lang_texts[id];
		else console.log("Element with id (" + id + ") not found!");
	}
}